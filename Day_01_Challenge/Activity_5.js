// Declare a variable with const and assign an initial value
const myConstant = 10;
console.log("Initial value:", myConstant); // Logs: Initial value: 10

// Try to reassign a new value to the constant variable
try {
    myConstant = 25;
} catch (error) {
    console.error("Error:", error.message); 
}









