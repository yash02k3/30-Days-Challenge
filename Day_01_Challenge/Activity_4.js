//Declare a variable using let , assign it an initial value, reassign a new value and log both value to the console 

// Declare a variable and assign an initial value
let myVariable = 10;
console.log("Initial value:", myVariable); // Logs: Initial value: 10

// Reassign a new value to the variable
myVariable = 25;
console.log("New value:", myVariable); // Logs: New value: 25
