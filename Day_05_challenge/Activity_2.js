//Task 3: Write a function expression to find the maximum of two numbers and log the result to the console.
const findMax = function(a, b) {
    return a > b ? a : b;
};

// Example usage
console.log(findMax(5, 3)); // This will print 5
console.log(findMax(10, 15)); // This will print 15

//Task 4: Write a function expression to concatenate two strings and return the result.

const concatenateStrings = function(str1, str2) {
    return str1 + str2;
};

// Example usage
console.log(concatenateStrings('Hello, ', 'world!')); // This will print "Hello, world!"
console.log(concatenateStrings('JavaScript ', 'is fun!')); // This will print "JavaScript is fun!"
