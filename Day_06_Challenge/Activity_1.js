//Activity 1: Array Creation and Access


//Task 1: Create an array of numbers from 1 to 5 and log the array to the console.

let numbersArray = [1, 2, 3, 4, 5];

// Logging the array to the console
console.log("Array of numbers:", numbersArray);



//Task 2: Access the first and last elements of the array and log them to the console.

let firstElement = numbersArray[0];

// Accessing the last element of the array
let lastElement = numbersArray[numbersArray.length - 1];

// Logging the first and last elements to the console
console.log("First element:", firstElement);
console.log("Last element:", lastElement);
